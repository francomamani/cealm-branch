	/**
	 * Update the specified resource in storage.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function update($id)
	{
		$category_mark = CategoryMark::find($id);
		$category_mark->description = Input::get('description');
		$category_mark->percentage = Input::get('percentage');
		$category_mark->acronym = Input::get('acronym');
		$category_mark->save();
		return Response::json(array('success' => 'La categoria fue actualizada exitosamente'));
	}


	/**
	 * Remove the specified resource from storage.
