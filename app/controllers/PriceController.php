	/**
	 * Update the specified resource in storage.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function update($id)
	{
		$price = Price::find($id);
		$price->description = Input::get('description');
		$price->price = Input::get('price');
		$price->save();
		return Response::json('Precio actualizado');
	}


	/**
	 * Remove the specified resource from storage.
	 *
	 * @param  int  $id
	 * @return Response
	 */
